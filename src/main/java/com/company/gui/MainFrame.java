    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        String txt = searchTextField.getText();
        clearTable();
        if(txt!=null && !txt.isEmpty()){
            if(txt.contains(",")){
                String[] symptoms = txt.split(",");
                Set<Illness> illnesses = new HashSet<>();
                ArrayList<Symptom> symptomSet = (ArrayList<Symptom>) symptomRepository.findAll();
                Set<Illness> illnessSet = new HashSet<>();
                for (String s : symptoms){
                    symptomSet.stream().forEach(symptom -> {
                        if(symptom.getName().toLowerCase().contains(s.toLowerCase())){
                             illnessSet.add(symptom.getIllness());
                        }
                    });
                }
                
                showIllnesses(new ArrayList<>(illnessSet));
            }else{
                Set<Illness> illnesses = illnessRepository.getIllnessesBySymptom(txt);
                String s = "";
                for(Illness i : illnesses){
                    s = s + i.getName() + ",";
                }
                showIllnesses(new ArrayList<>(illnesses));
            }
                
        }
    }//GEN-LAST:event_searchBtnActionPerformed

