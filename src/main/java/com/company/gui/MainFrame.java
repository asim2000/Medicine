private void clearTable(){
    DefaultTableModel mainTable = (DefaultTableModel) mainTbl.getModel();
            mainTable.setRowCount(0);
            mainTable.setColumnCount(0);
            mainTbl.setVisible(false);
}
    private void showIllnesses(List<Illness> illnesses) {
        System.out.println(illnesses.size());
        if(illnesses != null && !illnesses.isEmpty()){
            mainTbl.setVisible(true);
        }
        DefaultTableModel mainTable = (DefaultTableModel) mainTbl.getModel();
            mainTable.setRowCount(0);
            mainTable.setColumnCount(0);
            for(Illness i : illnesses){
                mainTable.addColumn(i.getName());
                
            }
            Object[] object = new Object[illnesses.size()];
            for(int i=0;i<illnesses.size();i++){
                String s = "";
                for(int j=0;j<illnesses.get(i).getSymptoms().size();j++){
                    s = s + illnesses.get(i).getSymptoms().get(j).getName() + ",";
                }
                object[i] = s;
            }
            mainTable.addRow(object);
            mainTbl.setModel(mainTable);
            mainTable.fireTableDataChanged();
    }
